double interp(double input)
{
	double out;
	int i = 0;
	while (input >= sinTheta[i])
	{
		i++;
	}
	out = rho[i - 1] + ((input - sinTheta[i - 1]) / (sinTheta[i] - sinTheta[i - 1])) * (rho[i] - rho[i - 1]);
	return out;
}

